//------------------------------------------------------------------------------
// SoundID
//------------------------------------------------------------------------------
// 役割:
// サウンド再生システムで使用する識別子。
// BGM / SE を enum class で一元管理し、
// 再生・停止・ロード処理のキーとして使用する。
//
// 設計意図:
// 数値IDの直書きを避け、
// 型安全かつ可読性の高いサウンド指定を行うため。
// BGM と SE を同一 enum にまとめることで、
// 再生APIを共通化しやすくしている。
//
// 構成:
// - BGM_* : 背景音楽
// - SE_*  : 効果音
// - Count : ID総数（配列サイズ・ループ上限用）
//
// NOTE:
// - enum class の順序はID値として使用される前提。
// - Count は実体を持たない番兵であり、サウンドとして再生してはならない。
//------------------------------------------------------------------------------
#pragma once

/// サウンドID定義
/// - サウンド管理・再生のキーとして使用する
/// - 数値変換は内部実装に限定すること
enum class SoundID
{
    // --------------------------------------------------------------------------
    // BGM
    // --------------------------------------------------------------------------
    BGM_Title,        // タイトル画面BGM
    BGM_Game,         // ゲーム中BGM
    BGM_GameOver,     // ゲームオーバーBGM

    // --------------------------------------------------------------------------
    // SE
    // --------------------------------------------------------------------------
    SE_BallHit,       // ボール衝突音
    SE_EnemyDead,     // 敵撃破音

    /// サウンドID総数（番兵）
    Count,
};
